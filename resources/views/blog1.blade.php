        <p style="font-size: large;">
            The proposed programmed accident detection system can be a rescuer of life for the people who met with accidents.
            The proposed system is exceptionally easy to understand and even a non-specialized Person can use it without any problem.
            The system consists of equipment and programming  segments.
            The  equipment  unit  includes  accident  detection  sensors  that  are constrained by an Arduino board and is fitted in the vehicle.
            Then again, Android application introduced in  drivers Smartphones  which is used to  get the point by point map.
            In general, the benefits of this system are low cost, secure and simple to use.
            The system introduced in this work reduces the casualties due to accidents.
