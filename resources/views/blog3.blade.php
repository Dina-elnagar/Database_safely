    <h1 style="text-align: center; color: rgb(255, 174, 0);">How to Drive a Car? Guide to learn Car Driving for Beginners</h1>
    <br><br>
    <center><img src="C:\Users\hp\Desktop\Studyspace\projecttwo\Assets\Images\images (1).jfif" alt="drive test" height="650" width="700"></center>
    <br><br>
    <div class="p1">
        <p style="font-size: large;">
            When an individual is learning how to drive a car, there are several things that they should keep in mind. 
            Regardless of whether one is a seasoned or rookie driver, it is imperative to be aware of certain aspects when one is behind the steering wheel.
            Several individuals know how to drive an automatic car but are not comfortable behind the wheels of a manual one. 
            The opposite holds true as well. 
            Therefore, to help out all individuals, this article will illustrate all vital aspects of driving both these types of cars.
            Read to know more!
        </p>
    </div>
    <br><br>
        <div class="p2">
        <h3>What Are the Things to Do Before Driving a Car for the First Time?</h3>
        <p style="font-size: large;">
            Individuals searching on how to drive a car for beginners must first ensure to do the following things before starting to drive:
        </p>
            <ul style="font-size: large;">
            <li>Adjust driver seat and mirrors as per preference.</li>
            <li>Put on a seatbelt and engage the parking or emergency brake.</li>
            <li>Get familiarised with the gear shift pattern mentioned on the gear-lever and try a mock gear change to get the hang of it.</li> 
            <li>Besides this, one must also ensure that they are on a level field to avoid rolling back or ahead.</li> 
            </ul>
            <p style="font-size: large;">
            After doing all of these, individuals looking for answers on how to drive a car in India, can proceed with the steps to drive a manual and automatic car.
            </p>
            <br>
            </div>
            <div class="p3">
            <h3>What Are Some Tips to Remember While Driving a Car in Traffic?</h3>
            <p style="font-size: large;">
                Individuals searching for answers on how to drive a car in traffic, must remember some additional facts. Some of these are listed below:
            </p>
            <ul style="font-size: large;">
            <li>Since a clutch is a significant wear and tear component of cars driven in traffic, one must go easy on it. 
                Ideally, drivers must wait till the car ahead has moved a certain distance, shift to first gear, release the clutch fully and go forward. 
                While stopping in traffic, press the clutch, shift to neutral and then release the clutch.</li>
            <li>Use the internal mirror and external rear view mirrors in city traffic. 
                The mirror on the left side is particularly useful for spotting two-wheelers and others willing to overtake on that side.</li>
            <li>Individuals willing to take turns should plan in advance and use the indicators while shifting lanes.</li> 
            <li>Do not panic brake, as the vehicle will get hit on its rear-end by an approaching car. 
                Feather the brake when an approaching light on the rearview mirror changes to amber. 
                This will ensure that brake lights come on and indicate to the following traffic that the car will stop.</li>
            <li>Individuals must use a GPS as it will let one know the route, and one does not have to stop and hold up traffic to ask for directions.</li>  
            </ul>
            </div>
            <br><br>
            <center><img src="C:\Users\hp\Desktop\Studyspace\projecttwo\Assets\Images\DrivingACar-780x480.jpg" alt="drive test" height="650" width="700"></center>
