    <h1 style="text-align: center; color: rgb(255, 174, 0);">Discover Why You Need Safely Mobile App</h1>
    <br><br>
    <center><img src="C:\Users\hp\Desktop\Studyspace\projecttwo\Assets\Images\1.jfif" alt="safelyApp" height="650" width="700"></center>
    <br><br>
    <div class="p1">
        <p style="font-size: large;">
        Nowadays,  the  rate  of accidents  has increased  rapidly.
        Due  to  employment, the  usage of vehicles like cars, bikes have increased, because of this reason the accidents can happen due to over speed.
        People are going under risk because of their over speed, due to unavailability of advanced techniques, the rate of accidents canâ€™t be decreased.
        To reduce the accident rate in  the  country  this  paper  introduces  a  solution.  Automatic  accident  detection  and  alert systems are introduced.
        The main objective is to control the accidents by sending a message to  the  registered  mobile,  hospital  and  police  station  using  wireless  communications techniques.
        When  an accident  occurs  in  a  city  or  any  place,  the  message  is  sent  to  the registered mobile through GSM module in less time.
        Arduino is the heart of the system which helps in transferring the message to different devices in the system.
        Vibration sensor will be activated when the accident occurs and the information is transferred to the registered number through the GSM module.
        The GPS system will help in finding the location of the accident spot.
        The proposed system will  check whether an accident has occurred  and notify nearest medical centers and registered mobile numbers about the place of accident using GSM and GPS modules. The location can be sent through a tracking system to cover the geographical coordinates over the area.
        The accident can be detected by a vibration sensor which is used as a major module in the system.
        </p>
    </div>
    <br><br>
    <center><img src="C:\Users\hp\Desktop\Studyspace\projecttwo\Assets\Images\acc.jpg" alt="car accident" height="650" width="700"></center>
    <br><br>
    <div class="p2">
        <p style="font-size: large;">
            In this  system  at  first,  we  worked  on  the  prevention  of vehicle  accident  and  even  after all  the  preventive  measures  applied  if  the  accident  occurs  the  system  detects  it.
            After the  detection  of  vehicle  accident,  the  system  automatically  reports  to  the  ambulance service  and  police  station  without  any  time  loss  so  that  the  casualty  might  not  loss his/her life due to lack of medical assistance in time. The system is installed in the vehicle..
            For the  detection of  vehicle accidents  accelerometers are  installed and  for  reporting ,GPS module and GSM  module are used.
            Motor  (control switch) is used  for engine control  and buzzer, led lights etc.
            are used for warning during prevention. All these devices are interfaced with  the  central  microcontroller  (Arduino  Uno)  unit.
            Accelerometer detects the occurrence of accident and sends signal to the microcontroller for further  functioning.
            The  GPS  module provides  the  location,  speed,  time  and date  of  the certain place where the vehicle is in the real time.
            If an accident occurs, the accelerometer detects it and location of accident is obtained using GPS, and finally sends the information to the ambulance service and police by the help of a GSM module.
            The  message obtained in mobile phone consists of the location of the accidental place in the form of google map link which will help the emergency units like ambulance service and police station to reach the casualty in time and rescue the lives.
            <br>
            1. The Arduino setup is installed in a vehicle's crash guard or in bumpers of the vehicle on each side.
            <br>
            2. When collision occurs it triggers the push button and it sends a notification to the Arduino Board.
            <br>
            3. Arduino will take this input and will convert to the SIM808.
            <br>
            4. The coordinates are shared through GSM.
            <br>
            5. Through GSM the notification is passed to the saved mobile number.
            <br>
            6. It contains the exact GPS location.
            <br>
            7. The application is used to know the route and location.
            <br>
            8. If the accident is not severe the person can turn off the buzzer and the device will come back to normal.
        </p>
    </div>
