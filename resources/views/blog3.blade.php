            Learning how to drive is a lot easier than it looks. It looks intimidating from the passenger's seat, or in films, but once you get behind the wheel and gently put your foot on the pedal, the process becomes very intuitive. 
            Learn to take things slow in the beginning and you'll be well on your way to mastering the basics. 
            This article assumes you'll be driving a vehicle with an automatic transmission. 
            If you're not driving an automatic, you'll need to read about the basics of driving stick-shift (manual transmission) instead, although the general process will still be similar.
